class ColEdge {
	/**
	 * This is a class to represent the graph's edges to be colored.
	 * Class was originally part of the main method provided for the project.
	 * @author Steven Kelk
	 */

	/**
	 * Starter node ID
	 */
	int u;

	/**
	 * Goal node ID
	 */
	int v;

	/**
	 * Quick printer method.
	 * @return a string for printing
	 */
	public String toString() {
		return "u: " + u + "\tv: " + v;
	} 
}